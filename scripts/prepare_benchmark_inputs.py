import argparse
import json
import shutil
from pathlib import Path

from benchmark_common import load_dataset_index


def _link_or_copy(src: Path, dst: Path, method: str) -> str:
    if dst.exists():
        return "existing"
    dst.parent.mkdir(parents=True, exist_ok=True)

    if method in {"hardlink", "auto"}:
        try:
            dst.hardlink_to(src)
            return "hardlink"
        except OSError:
            if method == "hardlink":
                raise

    if method in {"copy", "auto"}:
        shutil.copy2(src, dst)
        return "copy"

    raise ValueError(f"Unsupported link method: {method}")


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--dataset-name", required=True, choices=["omnidocbench", "mdpbench"])
    parser.add_argument("--dataset-root", required=True)
    parser.add_argument("--output-dir", required=True)
    parser.add_argument("--link-method", choices=["auto", "hardlink", "copy"], default="auto")
    parser.add_argument("--limit", type=int)
    parser.add_argument("--manifest-json", help="Optional sampled manifest JSON generated by sample_benchmark_manifest.py")
    args = parser.parse_args()

    dataset_index = load_dataset_index(args.dataset_name, args.dataset_root)
    output_dir = Path(args.output_dir).resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    manifest = []
    link_counts: dict[str, int] = {"hardlink": 0, "copy": 0, "existing": 0}

    if args.manifest_json:
        manifest_payload = json.loads(Path(args.manifest_json).read_text(encoding="utf-8"))
        sample_ids = [item["sample_id"] for item in manifest_payload.get("samples", [])]
        samples = [dataset_index[sample_id] for sample_id in sample_ids if sample_id in dataset_index]
    else:
        samples = sorted(dataset_index.values(), key=lambda item: item.sample_id)
        if args.limit:
            samples = samples[: args.limit]

    for sample in samples:
        src = Path(sample.image_abspath)
        dst = output_dir / f"{sample.sample_id}{src.suffix.lower()}"
        mode = _link_or_copy(src, dst, args.link_method)
        link_counts[mode] = link_counts.get(mode, 0) + 1
        manifest.append(
            {
                "sample_id": sample.sample_id,
                "source_image": str(src),
                "prepared_image": str(dst),
                "image_path": sample.image_path,
            }
        )

    summary = {
        "dataset_name": args.dataset_name,
        "dataset_root": str(Path(args.dataset_root).resolve()),
        "output_dir": str(output_dir),
        "sample_count": len(manifest),
        "link_method": args.link_method,
        "manifest_json": str(Path(args.manifest_json).resolve()) if args.manifest_json else None,
        "link_counts": link_counts,
        "samples": manifest,
    }
    (output_dir / "_prepared_manifest.json").write_text(
        json.dumps(summary, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    print(json.dumps({k: v for k, v in summary.items() if k != "samples"}, ensure_ascii=False, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
